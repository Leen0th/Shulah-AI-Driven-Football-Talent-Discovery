package com.shuela0.shuela0;

import java.util.List;

public class DATA {
    private String id;
    private String username;
    private String password;
    private String gmail;
    private String playerStatsID;
    private String role;
    private String position;
    private List<String> videoID;

    // Getter and Setter for ID
    public String getID() {
        return id;
    }

    public void setID(String id) {
        this.id = id;
    }

    // Getter and Setter for username
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    // Getter and Setter for password
    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    // Getter and Setter for gmail
    public String getGmail() {
        return gmail;
    }

    public void setGmail(String gmail) {
        this.gmail = gmail;
    }

    // Getter and Setter for playerStatsID
    public String getPlayerStatsID() {
        return playerStatsID;
    }

    public void setPlayerStatsID(String playerStatsID) {
        this.playerStatsID = playerStatsID;
    }

    // Getter and Setter for role
    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    // Getter and Setter for position
    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    // Getter and Setter for videoID
    public List<String> getVideoID() {
        return videoID;
    }

    public void setVideoID(List<String> videoID) {
        this.videoID = videoID;
    }
}
